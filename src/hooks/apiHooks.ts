const useUser = () => {
  const resourceUrl = import.meta.env.VITE_AUTH_API + '/users';

  const postRegister = async (inputs: RegisterCredentials) => {
    const fetchOptions = {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(inputs),
    };
    const registerResult = await fetchData(resourceUrl, fetchOptions);
    return registerResult;
  };

  const putUser = async (
    inputs: UserDetails,
    token: string,
  ): Promise<UserResponse> => {
    const fetchOptions = {
      method: 'PUT',
      headers: {
        'Content-Type': 'application/json',
        Authorization: 'Bearer ' + token,
      },
      body: JSON.stringify(inputs),
    };
    const putResult = await fetchData<UserResponse>(resourceUrl, fetchOptions);
    return putResult;
  };

  const getUserByToken = useCallback(
    async (token: string) => {
      const options = {
        method: 'GET',
        headers: {
          Authorization: 'Bearer ' + token,
        },
      };
      return fetchData<UserResponse>(resourceUrl + '/token', options);
    },
    [resourceUrl],
  );

  const getUsernameAvailable = async (username: string) => {
    // fetch from endpoint /users/username/:username
    return fetchData<AvailableResponse>(`${resourceUrl}/username/${username}`);
  };

  const getEmailAvailable = async (email: string) => {
    // fetch from endpoint /users/email/:email
    return fetchData<AvailableResponse>(`${resourceUrl}/email/${email}`);
  };

  const getUsers = useCallback(async (): Promise<UserWithNoPassword[]> => {
    return fetchData<UserWithNoPassword[]>(resourceUrl);
  }, [resourceUrl]);

  return {
    postRegister,
    putUser,
    getUserByToken,
    getUsernameAvailable,
    getEmailAvailable,
    getUsers,
  };
};

const useFile = () => {
  const postFile = async (file: File, token: string) => {
    // create FormData object
    const formData = new FormData();
    // add file to FormData
    formData.append('file', file);
    // upload the file to file server and get the file data
    // return the file data. The type is UploadResponse
    const options = {
      method: 'POST',
      headers: {
        Authorization: 'Bearer ' + token,
      },
      body: formData,
    };
    return fetchData<UploadResponse>(
      import.meta.env.VITE_UPLOAD_API + '/upload',
      options,
    );
  };

  return {postFile};
};

